// Helper functions for plugin filtering
export const getPluginsByCategory = (category: Plugin['category']) => 
  plugins.filter(p => p.category === category);

export const getDevOnlyPlugins = () => 
  plugins.filter(p => p.devOnly);

export const getDefaultPlugins = () => 
  plugins.filter(p => p.default);

export const getPluginsByTag = (tag: string) => 
  plugins.filter(p => p.tags?.includes(tag));

export const getPluginById = (id: string) => 
  plugins.find(p => p.id === id);
